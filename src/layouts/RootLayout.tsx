import type { Metadata } from 'next'
import { Inter } from 'next/font/google'
import './globals.css'
import { cookieToInitialState } from 'wagmi'
import { headers } from 'next/headers'
import ContextProvider from 'context/Web3Provider'
import { DefaultConfig } from 'config/connectors/DefaultConfig'

const inter = Inter({ subsets: ['latin'] })

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app'
}

export default function RootLayout({
  children
}: Readonly<{
  children: React.ReactNode
}>) {
  const initialState = cookieToInitialState(DefaultConfig, headers().get('cookie'))
  return (
    <html lang="en">
      <body className={inter.className}>
        {/* @ts-ignore */}
        <ContextProvider initialState={initialState}>{children}</ContextProvider>
      </body>
    </html>
  )
}